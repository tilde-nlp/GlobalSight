CREATE TABLE REMOVED_TAG
(
  ID BIGINT
     AUTO_INCREMENT
	 PRIMARY KEY,	 
  PREFIX_STRING 
     VARCHAR(4000)
	 NOT NULL,	 
  SUFFIX_STRING 
     VARCHAR(4000)
	 NOT NULL,
  TU_ID BIGINT
     NOT NULL,
	 CONSTRAINT FK_REMOVED_TAG_TU_ID FOREIGN KEY(TU_ID) REFERENCES TRANSLATION_UNIT(ID) 
) AUTO_INCREMENT = 1000;