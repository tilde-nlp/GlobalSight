CREATE TABLE IF NOT EXISTS XML_DTD
(
  ID BIGINT
     AUTO_INCREMENT
     PRIMARY KEY,
  NAME VARCHAR(40)
     NOT NULL,
  COMPANY_ID BIGINT
     NOT NULL,
  DESCRIPTION VARCHAR(4000),
  ADD_COMMENT CHAR(1)
    NOT NULL
    CHECK(ADD_COMMENT IN ('Y', 'N')),
  SEND_EMAIL CHAR(1)
    NOT NULL
    CHECK(SEND_EMAIL IN ('Y', 'N')),
  CONSTRAINT FK_XML_DTD_CASCADE_COMPANY_ID FOREIGN KEY(COMPANY_ID) REFERENCES COMPANY(ID) 
) AUTO_INCREMENT = 1000;

ALTER TABLE FILE_PROFILE ADD COLUMN XML_DTD_ID BIGINT;
