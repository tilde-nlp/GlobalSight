# GBS-4093 : MindTouch Connector V2

ALTER TABLE connector_mindtouch ADD COLUMN IS_POST_TO_SOURCE_SERVER VARCHAR(1) DEFAULT 'N';

CREATE TABLE CONNECTOR_MINDTOUCH_TARGET_SERVER(
  ID BIGINT AUTO_INCREMENT PRIMARY KEY,
  TARGET_LOCALE VARCHAR(50) NOT NULL,
  URL VARCHAR(300) NOT NULL,
  USER_NAME VARCHAR(300) NOT NULL,
  PASSWORD VARCHAR(300) NOT NULL,
  SOURCE_SERVER_ID BIGINT NOT NULL,
  IS_ACTIVE CHAR(1) NOT NULL CHECK (IS_ACTIVE IN ('Y', 'N')),
  COMPANY_ID BIGINT(20) UNSIGNED NOT NULL,
  CONSTRAINT FK_MINGTOUCH_CONNECTOR_ID FOREIGN KEY (SOURCE_SERVER_ID) REFERENCES CONNECTOR_MINDTOUCH(ID)
) ENGINE=INNODB;