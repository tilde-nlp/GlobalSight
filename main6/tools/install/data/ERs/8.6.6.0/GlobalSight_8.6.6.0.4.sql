# GBS-4154: Blaise Connector

CREATE TABLE CONNECTOR_BLAISE
(
  ID BIGINT(20) NOT NULL AUTO_INCREMENT,
  NAME VARCHAR(300) NOT NULL,
  DESCRIPTION VARCHAR(300) DEFAULT NULL,
  URL VARCHAR(300) NOT NULL,
  USER_NAME VARCHAR(300) NOT NULL,
  PASSWORD VARCHAR(300) NOT NULL,
  CLIENT_CORE_VERSION VARCHAR(30) NOT NULL,
  CLIENT_CORE_REVISION SMALLINT DEFAULT 0,
  WORKFLOW_ID VARCHAR(30) DEFAULT 'TRANSLATION',
  IS_ACTIVE CHAR(1) NOT NULL,
  COMPANY_ID BIGINT(20) UNSIGNED NOT NULL,
  PRIMARY KEY (ID)
) ENGINE=INNODB;

CREATE TABLE CONNECTOR_BLAISE_JOB (
  ID BIGINT(20) NOT NULL AUTO_INCREMENT,
  BLAISE_CONNECTOR_ID BIGINT(20) DEFAULT NULL,
  BLAISE_ENTRY_ID BIGINT(20) DEFAULT NULL,
  JOB_ID BIGINT(20) DEFAULT NULL,
  PRIMARY KEY (ID)
) ENGINE=INNODB;

CREATE INDEX IDX_JOB_ID ON CONNECTOR_BLAISE_JOB(JOB_ID);

ALTER TABLE job ADD COLUMN JOB_TYPE VARCHAR(30) DEFAULT NULL;
