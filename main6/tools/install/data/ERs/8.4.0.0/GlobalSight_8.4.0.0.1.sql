# GBS-2847, option to lock ICE or 100% or both
ALTER TABLE L10N_PROFILE ADD COLUMN TM_EDIT_TYPE INT(1) DEFAULT 2;

UPDATE L10N_PROFILE SET TM_EDIT_TYPE = 0 WHERE IS_EXACT_MATCH_EDIT = 'N' and TM_CHOICE=0;
UPDATE L10N_PROFILE SET TM_EDIT_TYPE = 1 WHERE IS_EXACT_MATCH_EDIT = 'N' and TM_CHOICE=2;
UPDATE L10N_PROFILE SET TM_EDIT_TYPE = 2 WHERE IS_EXACT_MATCH_EDIT = 'Y';
