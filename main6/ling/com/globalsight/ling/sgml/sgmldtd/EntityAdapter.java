package com.globalsight.ling.sgml.sgmldtd;

import com.globalsight.ling.sgml.GlobalSightEntity;

/**
 * An interface that all DTD entities must provide so that GlobalSight
 * can use them.
 */
public class EntityAdapter implements GlobalSightEntity
{
    public String name;
    public String value;

    public EntityAdapter(String name, String value)
    {
        this.name = name;
        this.value = value;
    }

    public String getName()
    {
        return name;
    }

    public String getValue()
    {
        return value;
    }
}
