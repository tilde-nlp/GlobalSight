/**
 * <p>http://www.oasis-open.org/cover/isoEntsExplained.html
 *
 * <p>'ISO entity files' - Explained by Rick Jelliffe
 *
 * <p>Someone on this list has asked what ISOdia, ISOtech etc are.
 *
 * <p>The SGML standard (ISO 8879) included several sets of entity
 * definitions for many special characters:
 *
 * <ul>
 * <li>ISOlat1 gives the characters in extended Latin alphabet #1,
 *             which is also the upper part of ISO 8859-1
 * <li>ISOlat2 gives a whole lot of extra latin characters
 * <li>ISOgrk1 and ISOgrk2 give simple modern Greek characters
 * <li>ISOcyr2 and ISOcyr2 give modern Russian and non-Russian Cyrillic characters
 * <li>ISOdia  gives spacing versions of diacritical marks
 *             (and is therefore not very useful, I think)
 * <li>ISOpub, ISOtech give symbols used in publishing and science
 * <li>ISOnum, ISOgrk3 and ISOgrk4 give symbols used in mathematics
 * <li>ISObox  gives the box characters (yuck)
 * </ul>
 *
 * <p>These entity sets allow you to use special characters in your
 * document, regardless of what the document character set you are
 * using. Well known examples are "&lt;", "&amp;", or "&mdash;".
 *
 * <p>They are "SDATA" entity sets, which means that it is the job of the
 * recipient to map them to something locally useful. XML uses ISO
 * 10646 (=Unicode) as its document character set, so I made up
 * versions of some of the public entity sets resolved for use with
 * ISO 10646. That was the sets I posted.
 *
 * <p>The ISO standard character entity sets are almost universally used
 * in SGML documents, and giving the XML versions of them makes
 * translation from SGML to XML easier.
 *
 * <p>W3C has put out its own versions (HTMLsymbol, HTMLlat2, and
 * HTMLmisc) which contain have a selection of the most ubiquitous
 * special characters from the ISO sets: basically, the characters in
 * the so-called ANSI code page used on Windows and the Adobe Symbol
 * font, again resolved for ISO 10646.
 *
 * <p>[...]
 *
 * @see http://www.altheim.com/specs/charents.html for SDATA entities.
 */
